-- Database

DROP DATABASE IF EXISTS DS3;
CREATE DATABASE DS3;
USE DS3;


-- Tables

CREATE TABLE CUSTOMERS
  (
  CUSTOMERID INT NOT NULL AUTO_INCREMENT PRIMARY KEY, 
  FIRSTNAME VARCHAR(50) NOT NULL, 
  LASTNAME VARCHAR(50) NOT NULL, 
  ADDRESS1 VARCHAR(50) NOT NULL, 
  ADDRESS2 VARCHAR(50), 
  CITY VARCHAR(50) NOT NULL, 
  STATE VARCHAR(50), 
  ZIP INT, 
  COUNTRY VARCHAR(50) NOT NULL, 
  REGION TINYINT NOT NULL,
  EMAIL VARCHAR(50),
  PHONE VARCHAR(50),
  CREDITCARDTYPE INT NOT NULL,
  CREDITCARD VARCHAR(50) NOT NULL, 
  CREDITCARDEXPIRATION VARCHAR(50) NOT NULL, 
  USERNAME VARCHAR(50) NOT NULL, 
  PASSWORD VARCHAR(50) NOT NULL, 
  AGE TINYINT, 
  INCOME INT,
  GENDER VARCHAR(1) 
  )
  ENGINE = InnoDB;
  
CREATE TABLE CUST_HIST
  (
  CUSTOMERID INT NOT NULL, 
  ORDERID INT NOT NULL, 
  PROD_ID INT NOT NULL 
  )
  ENGINE = InnoDB;

CREATE TABLE MEMBERSHIP
  (
  CUSTOMERID INT NOT NULL,
  MEMBERSHIPTYPE INT NOT NULL,
  EXPIREDATE DATE NOT NULL
  )
  ENGINE = InnoDB;
  
CREATE TABLE ORDERS
  (
  ORDERID INT NOT NULL AUTO_INCREMENT PRIMARY KEY, 
  ORDERDATE DATE NOT NULL, 
  CUSTOMERID INT, 
  NETAMOUNT NUMERIC(12,2) NOT NULL, 
  TAX NUMERIC(12,2) NOT NULL, 
  TOTALAMOUNT NUMERIC(12,2) NOT NULL
  )
  ENGINE = InnoDB; 
  
CREATE TABLE ORDERLINES
  (
  ORDERLINEID SMALLINT NOT NULL, 
  ORDERID INT NOT NULL, 
  PROD_ID INT NOT NULL, 
  QUANTITY SMALLINT NOT NULL, 
  ORDERDATE DATE NOT NULL
  )
  ENGINE = InnoDB; 
  
CREATE TABLE PRODUCTS
  (
  PROD_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY, 
  CATEGORY TINYINT NOT NULL, 
  TITLE VARCHAR(50) NOT NULL, 
  ACTOR VARCHAR(50) NOT NULL, 
  PRICE NUMERIC(12,2) NOT NULL, 
  SPECIAL TINYINT,
  COMMON_PROD_ID INT NOT NULL,
  MEMBERSHIP_ITEM INT NOT NULL 
  )
  ENGINE = MyISAM;

CREATE TABLE REVIEWS
  (
  REVIEW_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  PROD_ID INT NOT NULL,
  REVIEW_DATE DATE NOT NULL,
  STARS INT NOT NULL,
  CUSTOMERID INT NOT NULL,
  REVIEW_SUMMARY VARCHAR(50) NOT NULL,
  REVIEW_TEXT VARCHAR(1000) NOT NULL
  )
  ENGINE = InnoDB;

CREATE TABLE REVIEWS_HELPFULNESS
  (
  REVIEWS_HELPFULNESS_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  REVIEW_ID INT NOT NULL,
  CUSTOMERID INT NOT NULL,
  HELPFULNESS INT NOT NULL
  ) 
  ENGINE = InnoDB;

CREATE TABLE INVENTORY
  (
  PROD_ID INT NOT NULL PRIMARY KEY,
  QUAN_IN_STOCK INT NOT NULL,
  SALES INT NOT NULL
  )
  ENGINE = InnoDB;
   

CREATE TABLE CATEGORIES
  (
  CATEGORY TINYINT NOT NULL AUTO_INCREMENT PRIMARY KEY, 
